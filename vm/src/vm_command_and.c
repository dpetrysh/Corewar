#include "corewar.h"

void				vm_command_and(t_c *c, t_pc *pc)
{
	(void)c;
	pc->reg[pc->val[2] - 1] = (pc->val[0] & pc->val[1]);
	pc->carry = (pc->reg[pc->val[2] - 1] ? 0 : 1);
}
